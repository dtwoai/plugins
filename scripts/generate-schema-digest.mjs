#!/usr/bin/env node
/**
 * Generates a compact markdown digest from the gateway-config schema artifact
 * and writes it into SKILL.md between sentinel markers.
 *
 * The digest exists because production Claude Code does NOT auto-load a
 * skill's `references/` directory; only SKILL.md is in the cached system
 * prompt. To get field-level schema grounding to real users, the digest
 * must live inline in SKILL.md.
 *
 * Usage:
 *   node scripts/generate-schema-digest.mjs                 # rewrite digest in SKILL.md
 *   node scripts/generate-schema-digest.mjs --check         # CI: exit 1 if digest is stale
 *   node scripts/generate-schema-digest.mjs --schema=PATH --skill=PATH
 *
 * Defaults (relative to repo root):
 *   --schema = dtwo/skills/dtwo-gateway-config/schema-reference.json
 *   --skill  = dtwo/skills/dtwo-gateway-config/SKILL.md
 *
 * Determinism: output is a pure function of the input artifact. Running
 * twice on the same input produces byte-identical SKILL.md content.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const BEGIN_MARKER = '<!-- BEGIN SCHEMA DIGEST (generated by scripts/generate-schema-digest.mjs — do not edit by hand) -->';
const END_MARKER = '<!-- END SCHEMA DIGEST -->';
const DEFAULT_SCHEMA = 'dtwo/skills/dtwo-gateway-config/schema-reference.json';
const DEFAULT_SKILL = 'dtwo/skills/dtwo-gateway-config/SKILL.md';

function parseArgs(argv) {
  const out = { check: false, schema: DEFAULT_SCHEMA, skill: DEFAULT_SKILL };
  for (const a of argv) {
    if (a === '--check') out.check = true;
    else if (a.startsWith('--schema=')) out.schema = a.slice('--schema='.length);
    else if (a.startsWith('--skill=')) out.skill = a.slice('--skill='.length);
    else if (a === '--help' || a === '-h') {
      console.log(
        [
          'Usage: node scripts/generate-schema-digest.mjs [--check] [--schema=PATH] [--skill=PATH]',
          '',
          '  --check          Exit 1 if SKILL.md digest is stale (does not write).',
          '  --schema=PATH    Override default schema path.',
          '  --skill=PATH     Override default SKILL.md path.',
        ].join('\n'),
      );
      process.exit(0);
    } else {
      console.error(`Unknown argument: ${a}`);
      process.exit(2);
    }
  }
  return out;
}

// --- audience filter (mirrors d2-skill-harness audienceFilter.ts) -----------

function hasDescendantSection(sectionPath, allSections) {
  if (sectionPath === '') return false;
  const prefix = `${sectionPath}.`;
  for (const s of allSections) {
    if (s.path !== sectionPath && s.path.startsWith(prefix)) return true;
  }
  return false;
}

function filterArtifactForSkill(artifact) {
  const copy = structuredClone(artifact);
  const keptSections = [];
  for (const section of copy.sections) {
    const filteredFields = section.fields.filter(f => f.audience === 'user');
    const hasVariants = Array.isArray(section.variants) && section.variants.length > 0;
    const hasDescendants = hasDescendantSection(section.path, copy.sections);
    const keepEmpty = hasVariants || hasDescendants || section.path === '';
    if (filteredFields.length === 0 && !keepEmpty) continue;
    keptSections.push({ ...section, fields: filteredFields });
  }
  return { ...copy, sections: keptSections };
}

// --- helpers ---------------------------------------------------------------

function findSection(filtered, path) {
  const s = filtered.sections.find(x => x.path === path);
  if (!s) throw new Error(`Missing section in filtered artifact: ${path}`);
  return s;
}

function fieldByName(section, name) {
  return section.fields.find(f => f.name === name);
}

function enumList(field) {
  if (!field?.enumValues) return '';
  return field.enumValues.map(v => `\`${v}\``).join('/');
}

function tickQuotes(s) {
  // Convert "identifier" → `identifier` in artifact-provided constraint
  // messages so they render as code in markdown. Conservative: only single-
  // word identifiers (no spaces).
  return s.replace(/"([A-Za-z0-9_]+)"/g, '`$1`');
}

function escapePipe(s) {
  return String(s).replace(/\|/g, '\\|').replace(/\r?\n/g, ' ');
}

function renderConstraints(constraints) {
  if (!Array.isArray(constraints) || constraints.length === 0) return '';
  return constraints
    .map(c => `> ${tickQuotes(c.message ?? '')}`)
    .join('\n>\n');
}

// --- renderers (Design C structure) ----------------------------------------

function renderSectionsAtAGlance() {
  return [
    '#### Sections at a glance',
    '',
    '| Section path | Synopsis |',
    '|---|---|',
    '| `gateway` | Gateway-wide settings (auth, SSRF, logging). Carries the `advanced` escape hatch and `log_level`. |',
    '| `gateway.authentication` | Inbound auth from clients to the gateway. `enabled` defaults to `true`; cross-field constraint requires `jwks_info` when enabled. |',
    '| `gateway.authentication.jwks_info` | Inbound JWT validation parameters. All four fields required when this object is present. |',
    '| `gateway.ssrf` | SSRF protection overrides. Strict defaults apply when omitted. |',
    '| `mcp_servers[]` | One entry per upstream MCP server. `name` and `url` required. |',
    '| `mcp_servers[].authentication` | Discriminated union keyed on `type`; outbound auth from gateway to the upstream server. Seven variants (see table). |',
    '',
  ].join('\n');
}

function renderGatewayAuthentication(filtered) {
  const auth = findSection(filtered, 'gateway.authentication');
  const enabled = fieldByName(auth, 'enabled');
  const ssoIssuer = fieldByName(auth, 'sso_issuer');
  const ssoScope = fieldByName(auth, 'sso_generic_scope');
  const constraintBlock = renderConstraints(auth.crossFieldConstraints);

  return [
    '#### `gateway.authentication` — load-bearing defaults & constraints',
    '',
    `- **\`enabled\`** — \`schemaDefault: ${JSON.stringify(enabled.schemaDefault)}\`. Type \`${enabled.type}\`. Target env var: \`${enabled.target}\`. **When omitted, the gateway authenticates incoming requests.** Set \`false\` only for local development.`,
    `- **\`sso_issuer\`** — optional \`${ssoIssuer.type}\`. Metadata-only; does NOT validate tokens by itself. Target: \`${ssoIssuer.target}\`.`,
    `- **\`sso_generic_scope\`** — optional \`${ssoScope.type}\`. Ignored unless \`sso_issuer\` is set. Target: \`${ssoScope.target}\`.`,
    '',
    '**Cross-field constraint** (verbatim from artifact):',
    constraintBlock,
    '',
  ].join('\n');
}

function renderJwksInfo(filtered) {
  const jwks = findSection(filtered, 'gateway.authentication.jwks_info');
  const rows = ['jwt_algorithm', 'jwt_jwks_uri', 'jwt_issuer', 'jwt_audience'].map(n => {
    const f = fieldByName(jwks, n);
    const typeCell = f.enumValues ? `enum: ${enumList(f)}` : f.type;
    return `| \`${f.name}\` | ${escapePipe(typeCell)} | \`${f.target}\` | ${escapePipe(f.rationale ?? f.description)} |`;
  });

  return [
    '#### `gateway.authentication.jwks_info` — inbound JWT validation',
    '',
    'All four fields are required when this object is present. These govern the **inbound** leg (clients → gateway), independent of any `mcp_servers[].authentication` block which governs the **outbound** leg (gateway → upstream MCP server). Populate `jwks_info` whenever the prompt supplies an IdP tenant + audience, even when the upstream server uses OAuth/DCR or "does not accept bearer tokens" — those statements describe the outbound leg only.',
    '',
    '| Field | Type | Target env | Rationale (from artifact) |',
    '|---|---|---|---|',
    ...rows,
    '',
  ].join('\n');
}

function renderSsrf(filtered) {
  const ssrf = findSection(filtered, 'gateway.ssrf');
  const rows = ssrf.fields.map(f => {
    const def = f.deployDefault !== null && f.deployDefault !== undefined
      ? `\`${JSON.stringify(f.deployDefault)}\``
      : '—';
    return `| \`${f.name}\` | ${escapePipe(f.type)} | ${def} | ${escapePipe(f.rationale ?? f.description)} |`;
  });

  return [
    '#### `gateway.ssrf` — strict-by-default',
    '',
    'Strict defaults block localhost, private networks, and fail-closed DNS when omitted.',
    '',
    '| Field | Type | deployDefault | Rationale (from artifact) |',
    '|---|---|---|---|',
    ...rows,
    '',
  ].join('\n');
}

function renderGatewayAdvancedAndLog(filtered) {
  const gw = findSection(filtered, 'gateway');
  const adv = fieldByName(gw, 'advanced');
  const log = fieldByName(gw, 'log_level');
  const logDefault = log.deployDefault !== null && log.deployDefault !== undefined
    ? `\`${log.deployDefault}\``
    : '—';

  return [
    '#### `gateway.advanced` and `gateway.log_level`',
    '',
    `- **\`advanced\`** — \`${adv.type}\`, \`targetKind: ${adv.targetKind}\`. Lines are appended verbatim to the deployed env file under systemd \`EnvironmentFile\` semantics (last-occurrence-wins). Validation rejects keys already emitted by a typed field, so it cannot shadow a typed field by accident. Keys here are case-sensitive — preserve exact casing.`,
    `- **\`log_level\`** — enum: ${enumList(log)}. \`deployDefault\`: ${logDefault}. Target: \`${log.target}\`.`,
    '',
  ].join('\n');
}

function renderMcpServersTop(filtered) {
  const mcp = findSection(filtered, 'mcp_servers[]');
  const rows = mcp.fields.map(f => {
    const req = f.required ? 'yes' : 'no';
    const typeCell = f.enumValues ? `enum: ${enumList(f)}` : f.type;
    const target = f.target ? `\`${f.target}\`` : '—';
    const notes = f.rationale ?? f.description ?? '';
    return `| \`${f.name}\` | ${req} | ${escapePipe(typeCell)} | ${target} | ${escapePipe(notes)} |`;
  });

  return [
    '#### `mcp_servers[]` — required and optional top-level fields',
    '',
    '| Field | Required | Type | Target | Notes (from artifact) |',
    '|---|---|---|---|---|',
    ...rows,
    '',
  ].join('\n');
}

function renderAuthVariants(filtered) {
  const auth = findSection(filtered, 'mcp_servers[].authentication');
  const variantNotes = {
    bearer: 'Static bearer token in `Authorization` header.',
    basic: 'HTTP basic auth.',
    authheaders: 'Array of `{key, value}` header pairs; each pair both required.',
    query_param: 'Auth via URL query parameter.',
    oauth: 'See the cross-field constraint below — `issuer`-OR-trio rule.',
    cert: 'PEM-encoded CA cert; used for custom-CA / mTLS / self-signed.',
    none: 'Explicitly disabled auth.',
  };
  const rows = (auth.variants ?? []).map(v => {
    const reqList = v.requiredFields.map(r => `\`${r}\``).join(', ');
    let notes = variantNotes[v.name];
    if (notes === undefined) {
      console.warn(`Warning: no hardcoded note for auth variant "${v.name}"; rendering with path fallback.`);
      notes = `(see schema artifact at \`${v.path}\`)`;
    }
    return `| \`${v.name}\` | ${reqList} | ${escapePipe(notes)} |`;
  });

  return [
    '#### `mcp_servers[].authentication` — variants',
    '',
    'Discriminated union keyed on `type`. `requiredFields[]` lists fields that MUST appear in the YAML for each variant (always includes `type` itself).',
    '',
    '| `type` | Required fields | Notes |',
    '|---|---|---|',
    ...rows,
    '',
  ].join('\n');
}

function renderOAuthVariant(filtered) {
  const oauth = findSection(filtered, 'mcp_servers[].authentication (oauth)');
  const constraintBlock = renderConstraints(oauth.crossFieldConstraints);

  // Required (variant) fields per requiredFields[]
  const parent = findSection(filtered, 'mcp_servers[].authentication');
  const oauthVariant = (parent.variants ?? []).find(v => v.name === 'oauth');
  const variantRequired = new Set(oauthVariant?.requiredFields ?? []);

  // Cross-field-constraint conditional fields
  const conditional = new Set(['issuer', 'client_id', 'client_secret', 'token_url']);

  const fieldOrder = [
    'grant_type', 'scopes',
    'issuer', 'client_id', 'client_secret', 'token_url',
    'authorization_url', 'redirect_uri', 'pkce_enabled',
  ];
  const rows = fieldOrder
    .map(name => fieldByName(oauth, name))
    .filter(Boolean)
    .map(f => {
      let req = 'no';
      if (variantRequired.has(f.name)) req = 'yes (variant)';
      else if (conditional.has(f.name)) req = 'conditional';
      const typeCell = f.secret ? `${f.type}, **secret**` : f.type;
      const rationale = f.rationale ?? f.description ?? '';
      return `| \`${f.name}\` | ${req} | ${escapePipe(typeCell)} | ${escapePipe(rationale)} |`;
    });

  return [
    '#### OAuth variant — fields and the load-bearing cross-field rule',
    '',
    '**Cross-field constraint** (verbatim from artifact):',
    constraintBlock,
    '',
    'In other words: a valid `oauth` block must satisfy one of these two shapes:',
    '',
    '- **DCR shape:** `issuer` is set. `client_id`, `client_secret`, and `token_url` may be omitted — the gateway discovers/registers them.',
    '- **Static-credentials shape:** `client_id` AND `client_secret` AND `token_url` are all set. `issuer` is not required.',
    '',
    'Setting some but not all of `client_id` / `client_secret` / `token_url` without `issuer` is invalid. Both shapes still require `type: oauth`, `grant_type`, and `scopes`.',
    '',
    '| Field | Required | Type | Rationale (from artifact) |',
    '|---|---|---|---|',
    ...rows,
    '',
  ].join('\n');
}

function collectSecretFields(filtered) {
  const out = [];
  for (const section of filtered.sections) {
    for (const f of section.fields) {
      if (f.secret) out.push({ section: section.path, field: f.name });
    }
  }
  return out;
}

function renderSecretFields(filtered) {
  const secrets = collectSecretFields(filtered);
  const rows = secrets.map(s => `| \`${s.section}.${s.field}\` |`);

  return [
    '#### Secret-typed fields — must emit a placeholder, never a literal',
    '',
    'Every field marked `secret: true` in the artifact. Emit a self-describing placeholder (`REPLACE_WITH_<FIELD>`, `PLACEHOLDER_<FIELD>`, `YOUR_<FIELD>`, `CHANGE_ME`, or `${ENV_VAR}`); never an inferred or literal credential. Bare `PLACEHOLDER` does not count.',
    '',
    '| Path |',
    '|---|',
    ...rows,
    '',
  ].join('\n');
}

function renderTargetKind() {
  return [
    '#### `target` / `targetKind` — where values land',
    '',
    '- **`targetKind: envVar`** — value is written to the deployed env file under the named `target` (e.g. `MCP_REQUIRE_AUTH`, `JWT_AUDIENCE`, `SSRF_ALLOWED_NETWORKS`, `LOG_LEVEL`).',
    '- **`targetKind: sotwPath`** — value is written into the SOTW YAML at the named dotted path (e.g. `sotw.url`, `sotw.oauth_config.client_secret`). All `mcp_servers[]` fields land here.',
    '- **`targetKind: advanced`** — only `gateway.advanced`. Lines are **appended verbatim** to the deployed env file; the parser does not validate keys here. Case-sensitive; the user owns correctness.',
    '',
  ].join('\n');
}

function renderDigest(filtered) {
  const preamble = [
    '### Schema Digest',
    '',
    'This subsection is generated from `schema-reference.json` by `scripts/generate-schema-digest.mjs` — do not edit by hand. It captures field-level facts (types, defaults, required-ness, secret flags, cross-field rules, target env-var / SOTW paths); the surrounding SKILL.md prose handles workflow and rules. When the digest and the underlying schema artifact disagree, the artifact wins; regenerate this section to reconcile.',
    '',
  ].join('\n');

  return [
    preamble,
    renderSectionsAtAGlance(),
    renderGatewayAuthentication(filtered),
    renderJwksInfo(filtered),
    renderSsrf(filtered),
    renderGatewayAdvancedAndLog(filtered),
    renderMcpServersTop(filtered),
    renderAuthVariants(filtered),
    renderOAuthVariant(filtered),
    renderSecretFields(filtered),
    renderTargetKind(),
  ].join('\n').trimEnd();
}

// --- write-out -------------------------------------------------------------

function replaceBetweenMarkers(body, content) {
  const begin = body.indexOf(BEGIN_MARKER);
  const end = body.indexOf(END_MARKER);
  if (begin === -1) {
    throw new Error(
      `Sentinel BEGIN marker not found in SKILL.md.\nExpected marker: ${BEGIN_MARKER}\nAdd both BEGIN and END markers where the digest should land.`,
    );
  }
  if (end === -1) {
    throw new Error(`Sentinel END marker not found in SKILL.md.\nExpected marker: ${END_MARKER}`);
  }
  if (end < begin) {
    throw new Error(`END marker appears before BEGIN marker in SKILL.md.`);
  }
  const before = body.slice(0, begin + BEGIN_MARKER.length);
  const after = body.slice(end);
  return `${before}\n\n${content}\n\n${after}`;
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const schemaAbs = resolve(args.schema);
  const skillAbs = resolve(args.skill);

  let artifact;
  try {
    artifact = JSON.parse(readFileSync(schemaAbs, 'utf8'));
  } catch (e) {
    console.error(`Failed to read schema at ${schemaAbs}: ${e.message}`);
    process.exit(2);
  }

  const filtered = filterArtifactForSkill(artifact);
  const digest = renderDigest(filtered);

  const body = readFileSync(skillAbs, 'utf8');
  const updated = replaceBetweenMarkers(body, digest);

  if (args.check) {
    if (updated !== body) {
      console.error(
        [
          'SKILL.md schema digest is stale relative to schema-reference.json.',
          `Run: node scripts/generate-schema-digest.mjs`,
          `Or:  node scripts/generate-schema-digest.mjs --schema=${args.schema} --skill=${args.skill}`,
          'Then commit the updated SKILL.md.',
        ].join('\n'),
      );
      process.exit(1);
    }
    console.log('SKILL.md schema digest is in sync with schema-reference.json.');
    return;
  }

  if (updated !== body) {
    writeFileSync(skillAbs, updated);
    console.log(`Wrote updated digest to ${args.skill} (${updated.length} bytes total).`);
  } else {
    console.log(`No change — digest already current.`);
  }
}

main();
